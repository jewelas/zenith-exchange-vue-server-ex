<script>
const bitWindHomeFuncton = function bitWindHome() {
  const offsetheight = document.documentElement.clientHeight; // 获取当前页面的高度、
  const small = offsetheight <= 800;
  const $container = document.getElementById('bitWind-content');
  const $page = document.getElementById('bitWind-page');
  const $blockItem = document.getElementsByClassName('block-item');
  const $navItem = document.getElementsByClassName('nav-item');
  const $app = document.getElementById('app');
  const $pageUl = document.getElementById('page-ul');

  let fullPage = 0; // 当前在第几页
  let fullPageNum = false; // 是否在滑动

  if ($pageUl && small) {
    $pageUl.style.height = `${offsetheight - 100}px`;
  }
  if ($container && !small) {
    $container.style.height = `${offsetheight}px`;
  }
  if ($blockItem && $blockItem.length) {
    for (let i = $blockItem.length - 1; i >= 0; i -= 1) {
      $blockItem[i].style.height = `${offsetheight}px`;
    }
  }
  // 设置第一个为选中状态
  if ($navItem[fullPage]) $navItem[fullPage].classList.add('active');
  const setNavClass = () => {
    if ($navItem && $navItem.length) {
      for (let i = $navItem.length - 1; i >= 0; i -= 1) {
        $navItem[i].classList.remove('active');
        if (fullPage < 6) {
          $navItem[fullPage].classList.add('active');
        }
        if (fullPage === 6) {
          $app.classList.add('app-block-item7');
        } else {
          $app.classList.remove('app-block-item7');
        }
      }
    }
  };
  const setPage = () => {
    setNavClass();
    $page.style.transform = `translate3d(0px,-${fullPage * offsetheight}px, 0px)`;
  };
  const scroll = (e) => {
    const even = e || window.event;
    if (!fullPageNum) {
      if (even.wheelDelta < -10 && fullPage < $blockItem.length - 1) {
        fullPage += 1;
        fullPageNum = true;
        setPage();
        setTimeout(() => {
          fullPageNum = false;
        }, 1200);
      }
      if (even.wheelDelta > 10 && fullPage > 0) {
        fullPage -= 1;
        fullPageNum = true;
        setPage();
        setTimeout(() => {
          fullPageNum = false;
        }, 1200);
      }
    }
  };
  const clickPage = () => {
    if ($pageUl) {
      $pageUl.onclick = function navclck(e) {
        const index = parseInt(e.target.id, 0);
        if (small) {
          const navList = document.querySelector('#page-ul').querySelectorAll('.nav-item');
          document.querySelector('#bitWind-page').querySelectorAll('.block-item')[index].scrollIntoView();
          for (let i = 0, len = navList.length; i < len; i += 1) {
            navList[i].classList.remove('active');
          }
          navList[index].classList.add('active');
        } else {
          document.querySelector('#page-ul').querySelectorAll('.nav-item');
          fullPage = index;
          setPage();
        }
      };
    }
  };
  clickPage();
  if (!small) {
    if (document.addEventListener) {
      document.addEventListener('DOMMouseScroll', scroll, false);
    }
    document.onmousewheel = scroll;
    window.onmousewheel = document.onmousewheel;
  }
};
window.bitWindHomeFuncton = bitWindHomeFuncton;
</script>
